export type ToolName = (typeof RAW_TOOL_DEFINITIONS)[number]["name"];

export const TOOL_DEFINITIONS: ReadonlyArray<(typeof RAW_TOOL_DEFINITIONS)[number]> = [...RAW_TOOL_DEFINITIONS].sort(
  (a, b) => (TOOL_DEFINITION_RANK.get(a.name) ?? Number.MAX_SAFE_INTEGER) - (TOOL_DEFINITION_RANK.get(b.name) ?? Number.MAX_SAFE_INTEGER),
);

export const TOOL_SCOPES: Readonly<Record<ToolName, Scope>> = Object.fromEntries(
  TOOL_DEFINITIONS.map((tool) => [tool.name, tool.requiredScope]),
) as Record<ToolName, Scope>;

export function requiredScopeForTool(toolName: string): Scope | undefined {
  return (TOOL_SCOPES as Partial<Record<string, Scope>>)[toolName];
}

export function definitionForTool(toolName: string): ToolDefinition | undefined {
  return TOOL_DEFINITIONS.find((tool) => tool.name === toolName);
}

export function registerAllTools(server: McpServer, runtime: McpRuntime): void {
  for (const tool of TOOL_DEFINITIONS) {
    tool.register(server, runtime);
  }
}
